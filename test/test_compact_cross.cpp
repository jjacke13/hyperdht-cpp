TEST(CrossIpv6Addr, MatchesJS) {
    // Vectors from: node -e "require('compact-encoding-net').ipv6Address"
    struct Case { std::string host; uint16_t port; std::string hex; };
    std::vector<Case> cases = {
        {"2001:db8:0:0:0:0:0:1",              8080,  "20010db8000000000000000000000001901f"},
        {"fe80:0:0:0:0:0:0:1",                443,   "fe800000000000000000000000000001bb01"},
        {"0:0:0:0:0:0:0:1",                   0,     "000000000000000000000000000000010000"},
        {"0:0:0:0:0:0:0:0",                   1234,  "00000000000000000000000000000000d204"},
        {"2001:db8:85a3:0:0:8a2e:370:7334",   49737, "20010db885a3000000008a2e0370733449c2"},
    };

    for (const auto& c : cases) {
        auto addr = Ipv6Address::from_string(c.host, c.port);

        // C++ encode must match JS encode byte-for-byte
        EXPECT_EQ(encode_hex<Ipv6Addr>(addr), c.hex)
            << "ipv6addr encode mismatch for " << c.host << ":" << c.port;

        // C++ decode of JS bytes must produce correct host/port
        auto bytes = from_hex(c.hex);
        auto s = State::for_decode(bytes.data(), bytes.size());
        auto decoded = Ipv6Addr::decode(s);
        EXPECT_FALSE(s.error);
        EXPECT_EQ(decoded.host_string(), c.host)
            << "ipv6addr decode host mismatch for hex " << c.hex;
        EXPECT_EQ(decoded.port, c.port)
            << "ipv6addr decode port mismatch for hex " << c.hex;
    }
}

TEST(CrossIpv6Array, MatchesJS) {
    // Empty array
    {
        std::vector<Ipv6Address> addrs;
        EXPECT_EQ(encode_hex<Ipv6Array>(addrs), "00");
    }

    // Two addresses — hex from JS: c.array(net.ipv6Address).encode(...)
    {
        std::vector<Ipv6Address> addrs = {
            Ipv6Address::from_string("2001:db8::1", 8080),
            Ipv6Address::from_string("fe80::1", 443),
        };
        std::string expected = "0220010db8000000000000000000000001901f"
                               "fe800000000000000000000000000001bb01";
        EXPECT_EQ(encode_hex<Ipv6Array>(addrs), expected);

        // Decode
        auto bytes = from_hex(expected);
        auto s = State::for_decode(bytes.data(), bytes.size());
        auto decoded = Ipv6Array::decode(s);
        EXPECT_FALSE(s.error);
        ASSERT_EQ(decoded.size(), 2u);
        EXPECT_EQ(decoded[0].host_string(), "2001:db8:0:0:0:0:0:1");
        EXPECT_EQ(decoded[0].port, 8080);
        EXPECT_EQ(decoded[1].host_string(), "fe80:0:0:0:0:0:0:1");
        EXPECT_EQ(decoded[1].port, 443);
    }
}
