// ---------------------------------------------------------------------------
// Helper: hex string → byte vector
// ---------------------------------------------------------------------------
static std::vector<uint8_t> from_hex(const std::string& hex) {
    std::vector<uint8_t> bytes;
    bytes.reserve(hex.size() / 2);
    for (size_t i = 0; i < hex.size(); i += 2) {
        uint8_t hi = (hex[i] >= 'a') ? (hex[i] - 'a' + 10) : (hex[i] - '0');
        uint8_t lo = (hex[i + 1] >= 'a') ? (hex[i + 1] - 'a' + 10) : (hex[i + 1] - '0');
        bytes.push_back((hi << 4) | lo);
    }
    return bytes;
}

static std::string to_hex(const std::vector<uint8_t>& bytes) {
    static const char digits[] = "0123456789abcdef";
    std::string result;
    result.reserve(bytes.size() * 2);
    for (auto b : bytes) {
        result += digits[b >> 4];
        result += digits[b & 0xF];
    }
    return result;
}

// Helper: encode a value and return hex
template <typename Enc, typename T>
static std::string encode_hex(const T& v) {
    State s;
    Enc::preencode(s, v);
    std::vector<uint8_t> buf(s.end);
    s.buffer = buf.data();
    s.start = 0;
    Enc::encode(s, v);
    return to_hex(buf);
}

// ===========================================================================
// Cross-tests: C++ encode must produce identical bytes to JS encode
// Vectors generated by test/js/generate_test_vectors.js
// ===========================================================================

TEST(CrossVarint, MatchesJS) {
    struct Case { uint64_t value; std::string hex; };
    std::vector<Case> cases = {
        {0,            "00"},
        {1,            "01"},
        {42,           "2a"},
        {252,          "fc"},
        {253,          "fdfd00"},
        {255,          "fdff00"},
        {256,          "fd0001"},
        {4200,         "fd6810"},
        {65535,        "fdffff"},
        {65536,        "fe00000100"},
        {300000,       "fee0930400"},
        {4294967295,   "feffffffff"},
    };

    for (const auto& c : cases) {
        EXPECT_EQ(encode_hex<Uint>(c.value), c.hex)
            << "varint mismatch for value " << c.value;

        // Also verify decode
        auto bytes = from_hex(c.hex);
        auto s = State::for_decode(bytes.data(), bytes.size());
        auto decoded = Uint::decode(s);
        EXPECT_FALSE(s.error);
        EXPECT_EQ(decoded, c.value) << "decode mismatch for hex " << c.hex;
    }
}

TEST(CrossUint16, MatchesJS) {
    EXPECT_EQ(encode_hex<Uint16>(uint16_t{0}), "0000");
    EXPECT_EQ(encode_hex<Uint16>(uint16_t{0x1234}), "3412");
    EXPECT_EQ(encode_hex<Uint16>(uint16_t{0xFFFF}), "ffff");
}

TEST(CrossUint32, MatchesJS) {
    EXPECT_EQ(encode_hex<Uint32>(uint32_t{0}), "00000000");
    EXPECT_EQ(encode_hex<Uint32>(uint32_t{0xDEADBEEF}), "efbeadde");
}

TEST(CrossBool, MatchesJS) {
    EXPECT_EQ(encode_hex<Bool>(false), "00");
    EXPECT_EQ(encode_hex<Bool>(true), "01");
}

TEST(CrossBuffer, MatchesJS) {
    // Null
    {
        State s;
        Buffer::preencode_null(s);
        std::vector<uint8_t> buf(s.end);
        s.buffer = buf.data();
        s.start = 0;
        Buffer::encode_null(s);
        EXPECT_EQ(to_hex(buf), "00");
    }

    // Non-null: 0xDEADBEEF
    {
        auto payload = from_hex("deadbeef");
        State s;
        Buffer::preencode(s, payload.data(), payload.size());
        std::vector<uint8_t> buf(s.end);
        s.buffer = buf.data();
        s.start = 0;
        Buffer::encode(s, payload.data(), payload.size());
        EXPECT_EQ(to_hex(buf), "04deadbeef");
    }
}

TEST(CrossIpv4Addr, MatchesJS) {
    struct Case { std::string host; uint16_t port; std::string hex; };
    std::vector<Case> cases = {
        {"10.20.30.40",  0x1234, "0a141e283412"},
        {"88.99.3.86",   49737,  "5863035649c2"},
        {"192.168.1.1",  8080,   "c0a80101901f"},
    };

    for (const auto& c : cases) {
        auto addr = Ipv4Address::from_string(c.host, c.port);
        EXPECT_EQ(encode_hex<Ipv4Addr>(addr), c.hex)
            << "ipv4addr mismatch for " << c.host << ":" << c.port;

        // Decode
        auto bytes = from_hex(c.hex);
        auto s = State::for_decode(bytes.data(), bytes.size());
        auto decoded = Ipv4Addr::decode(s);
        EXPECT_FALSE(s.error);
        EXPECT_EQ(decoded.host_string(), c.host);
        EXPECT_EQ(decoded.port, c.port);
    }
}

TEST(CrossIpv4Array, MatchesJS) {
    // Empty array
    {
        std::vector<Ipv4Address> addrs;
        EXPECT_EQ(encode_hex<Ipv4Array>(addrs), "00");
    }

    // Two addresses
    {
        std::vector<Ipv4Address> addrs = {
            Ipv4Address::from_string("192.168.1.1", 8080),
            Ipv4Address::from_string("10.0.0.1", 443),
        };
        EXPECT_EQ(encode_hex<Ipv4Array>(addrs), "02c0a80101901f0a000001bb01");

        // Decode
        auto bytes = from_hex("02c0a80101901f0a000001bb01");
        auto s = State::for_decode(bytes.data(), bytes.size());
        auto decoded = Ipv4Array::decode(s);
        EXPECT_FALSE(s.error);
        ASSERT_EQ(decoded.size(), 2);
        EXPECT_EQ(decoded[0].host_string(), "192.168.1.1");
        EXPECT_EQ(decoded[0].port, 8080);
        EXPECT_EQ(decoded[1].host_string(), "10.0.0.1");
        EXPECT_EQ(decoded[1].port, 443);
    }
}

